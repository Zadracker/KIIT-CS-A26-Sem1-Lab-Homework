//Assign length and width of a rectangle and find its area and perimeter

#include <stdio.h>
int main() {
int length=1;
int width=2;
int perimeter,area;
perimeter=2*(length+width);
area=length*width;
printf("Perimeter: %d\n",perimeter);
printf("Area: %d\n",area);
return 0;}

//Output
/*
Perimeter: 6
Area: 2
*/

